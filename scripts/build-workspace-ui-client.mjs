import { build } from "esbuild";
import { existsSync, mkdirSync, renameSync, rmSync, writeFileSync } from "node:fs";
import { basename, dirname, resolve } from "node:path";

const entryPoint = resolve("src/workspace-ui/client/main.ts");
const outfile = resolve("dist/workspace-ui/client.js");
const metafilePath = resolve("dist/workspace-ui/client.meta.json");

if (!existsSync(entryPoint)) {
  console.error(`[build-workspace-ui-client] Missing input file: ${entryPoint}`);
  process.exit(1);
}

mkdirSync(dirname(outfile), { recursive: true });

const result = await build({
  entryPoints: [entryPoint],
  outfile,
  banner: {
    js: [
      "/*",
      " * Quailbot Workspace UI browser bundle.",
      " * Generated by scripts/build-workspace-ui-client.mjs from src/workspace-ui/client/main.ts.",
      " * Server routes must serve these built bytes from dist/workspace-ui/client.js; do not replace with an imported TS source string.",
      " */",
    ].join("\n"),
  },
  platform: "browser",
  format: "iife",
  globalName: "QuailbotWorkspaceUI",
  bundle: true,
  sourcemap: true,
  target: "es2022",
  minify: false,
  metafile: true,
  write: false,
});

const outputFiles = new Map(result.outputFiles.map((file) => [basename(file.path), file.contents]));
const bundle = outputFiles.get("client.js");
const sourceMap = outputFiles.get("client.js.map");
if (!bundle || !sourceMap) {
  throw new Error("[build-workspace-ui-client] esbuild did not return the expected client.js and client.js.map outputs");
}

writeAtomic(outfile, bundle);
writeAtomic(`${outfile}.map`, sourceMap);
writeAtomic(metafilePath, `${JSON.stringify(result.metafile, null, 2)}\n`);

function writeAtomic(path, contents) {
  const tmpPath = `${path}.tmp-${process.pid}-${Date.now()}-${Math.random().toString(16).slice(2)}`;
  try {
    writeFileSync(tmpPath, contents);
    renameSync(tmpPath, path);
  } catch (error) {
    rmSync(tmpPath, { force: true });
    throw error;
  }
}
